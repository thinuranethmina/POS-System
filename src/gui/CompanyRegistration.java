    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a company.", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String id = jTable1.getValueAt(r, 0).toString();
                String name = jTable1.getValueAt(r, 1).toString();

                br.jLabel8.setText(id);
                br.jLabel9.setText(name);
                br.LoadCompanyBranches();
                this.dispose();
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        // TODO add your handling code here:
        String mobile = jTextField2.getText();
        String text = mobile + evt.getKeyChar();

        if (mobile.length() == 10) {
            evt.consume();
        } else if (!Pattern.compile("[0-9]+").matcher(text).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String name = jTextField1.getText();
        String mobile = jTextField2.getText();

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter company name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[0][0-9]{9}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid contact number.", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            if (jButton1.getText().equals("Create New Company")) {
                try {

                    ResultSet rs0 = MySQL.search("SELECT * FROM `category` WHERE `name`='" + name + "' AND `mobile` = '" + mobile + "' ");

                    if (rs0.next()) {
                        JOptionPane.showMessageDialog(this, "This name or mobile number already exixts.", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {
                        ResultSet rs = MySQL.search("SELECT * FROM `company` WHERE `name`='" + name + "'");
                        if (rs.next()) {
                            JOptionPane.showMessageDialog(this, "Company already exists.", "Warning", JOptionPane.WARNING_MESSAGE);
                        } else {
                            MySQL.iud("INSERT INTO `company`(`name`,`mobile`) VALUES('" + name + "','" + mobile + "')");
                            LoadCompanies();
                            jTextField1.setText("");
                            jTextField2.setText("");
                            jTextField1.grabFocus();
                            JOptionPane.showMessageDialog(this, "New company added.", "Success", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            } else if (jButton1.getText().equals("Edit Details")) {
                try {
                    MySQL.iud("UPDATE `company` SET `name`='" + name + "',`mobile` = '" + mobile + "'  WHERE `id` = " + Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()) + "");
                    LoadCompanies();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField1.grabFocus();
                    jButton1.setText("Create New Company");
                    JOptionPane.showMessageDialog(this, "Company Details updated.", "Success", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

