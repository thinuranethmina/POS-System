    public void LoadCompanies() {

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `company` ORDER BY `id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("mobile"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void setTableListener() {

        ListSelectionListener lsl = (ListSelectionEvent arg0) -> {
            int selectedRow = jTable1.getSelectedRow();

            if (selectedRow != -1) {

                jButton1.setText("Edit Details");

                String id = jTable1.getValueAt(selectedRow, 0).toString();

                try {
                    ResultSet rs = MySQL.search("SELECT * FROM `company` WHERE `id`=" + Integer.parseInt(id) + "");
                    rs.next();
                    jTextField1.setText(rs.getString("name"));
                    jTextField2.setText(rs.getString("mobile"));

                } catch (Exception e) {
                    e.printStackTrace();
                }

            } else {
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField1.grabFocus();
                jButton1.setText("Create New Company");

            }
        };

        jTable1.getSelectionModel().addListSelectionListener(lsl);
    }

