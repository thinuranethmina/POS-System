    public void SearchStock() {
        try {

            String category = jComboBox2.getSelectedItem().toString();
            String brand = jComboBox1.getSelectedItem().toString();
            String name = jTextField1.getText();
            String sp_min = jTextField2.getText();
            String sp_max = jTextField4.getText();
            String qty_min = jTextField5.getText();
            String qty_max = jTextField6.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            String mfd_from = null;
            String mfd_to = null;
            String exd_to = null;
            String exd_from = null;

            text2 = "";

            if (jDateChooser1.getDate() != null) {
                mfd_from = sdf.format(jDateChooser1.getDate());
            }
            if (jDateChooser2.getDate() != null) {
                mfd_to = sdf.format(jDateChooser2.getDate());
            }
            if (jDateChooser3.getDate() != null) {
                exd_from = sdf.format(jDateChooser3.getDate());
            }
            if (jDateChooser4.getDate() != null) {
                exd_to = sdf.format(jDateChooser4.getDate());
            }

            int sort = jComboBox3.getSelectedIndex();

            Vector queryVector = new Vector();

            if (category.equals("-- S E L E C T --")) {
            } else {
                queryVector.add("`category`.`name` = '" + category + "'");
                text2 += category + " Category ";
            }

            if (brand.equals("-- S E L E C T --")) {

            } else {
                queryVector.add("`brand`.`name` = '" + brand + "'");
                text2 += brand + " Brand ";
            }

            if (name.isEmpty()) {

            } else {
                queryVector.add("`product`.`name` LIKE '%" + name + "%'");
                text2 += " Name LIKE " + name;
            }

            if (!sp_min.isEmpty()) {
                if (sp_max.isEmpty()) {
                    queryVector.add("`stock`.`selling_price` >= '" + sp_min + "'");
                    text2 += " Selleing price greater than or equal Rs." + sp_min;
                } else {
                    queryVector.add("`stock`.`selling_price` >= '" + sp_min + "' AND `stock`.`selling_price` <= '" + sp_max + "' ");
                    text2 += " Selleing price greater than  or equal Rs." + sp_min + " & Selleing price less than or equal Rs." + sp_max;
                }
            }

            if (!sp_max.isEmpty()) {
                if (sp_min.isEmpty()) {
                    queryVector.add("`stock`.`selling_price` <= '" + sp_max + "'");
                    text2 += " Selleing price less than or equal Rs." + sp_max;
                }
            }

            if (!qty_min.isEmpty()) {
                if (qty_max.isEmpty()) {
                    queryVector.add("`stock`.`quantity` >= '" + qty_min + "'");
                    text2 += " Quantity greater than or equal " + qty_min;
                } else {
                    queryVector.add("`stock`.`quantity` >= '" + qty_min + "' AND `stock`.`quantity` <= '" + qty_max + "' ");
                    text2 += " Quantity greater than or equal " + qty_min + " & Quantity less than or equal " + qty_max;
                }
            }

            if (!qty_max.isEmpty()) {
                if (qty_min.isEmpty()) {
                    queryVector.add("`stock`.`quantity` <= '" + qty_max + "'");
                    text2 += " Quantity less than or equal " + qty_max;
                }
            }

            if (mfd_from != null) {
                if (mfd_to == null) {
                    queryVector.add("`stock`.`mfd` >= '" + mfd_from + "'");
                    text2 += " Manufacture date after or equal " + mfd_from;
                } else {
                    queryVector.add("`stock`.`mfd` >= '" + mfd_from + "' AND `stock`.`mfd` <= '" + mfd_to + "' ");
                    text2 += " Manufacture date after or equal " + mfd_from + " & Manufacture date before or equal " + mfd_to;
                }

            }

            if (mfd_to != null) {
                if (mfd_from == null) {
                    queryVector.add("`stock`.`mfd` <= '" + mfd_to + "'");
                    text2 += " Manufacture date before or equal " + mfd_to;
                }
            }

            if (exd_from != null) {
                if (exd_to == null) {
                    queryVector.add("`stock`.`exd` >= '" + exd_from + "'");
                    text2 += " Expire date after or equal " + exd_from;
                } else {
                    queryVector.add("`stock`.`exd` >= '" + exd_from + "' AND `stock`.`exd` <= '" + exd_to + "' ");
                    text2 += " Expire date after or equal " + exd_from + " & Expire date before or equal " + exd_to;
                }

            }

            if (exd_to != null) {
                if (exd_from == null) {
                    queryVector.add("`stock`.`exd` <= '" + exd_to + "'");
                    text2 += " Expire date before or equal " + exd_to;
                }
            }

            String sortquery;

            String wherequery = "";

            for (int i = 0; i < queryVector.size(); i++) {
                if (i == 0) {
                    wherequery += "WHERE";
                }
                wherequery += " ";
                wherequery += queryVector.get(i);
                wherequery += " ";
                if (i != queryVector.size() - 1) {
                    wherequery += "AND";
                }
            }

            if (sort == 0) {
                sortquery = "`product`.`name` ASC ";
            } else if (sort == 1) {
                sortquery = "`product`.`name` DESC ";
            } else if (sort == 2) {
                sortquery = "`stock`.`selling_price` ASC ";
            } else if (sort == 3) {
                sortquery = "`stock`.`selling_price` DESC ";
            } else if (sort == 4) {
                sortquery = "`stock`.`quantity` ASC ";
            } else if (sort == 5) {
                sortquery = "`stock`.`quantity` DESC ";
            } else if (sort == 6) {
                sortquery = "`stock`.`exd` ASC ";
            } else {
                sortquery = "`stock`.`exd` DESC ";
            }

            ResultSet rs = MySQL.search("SELECT DISTINCT `stock`.`id`,`product`.`id`,`category`.`name`,`brand`.`name`,`product`.`name`,`stock`.`id`,`stock`.`quantity`,`stock`.`selling_price`,`grn_item`.`buying_price`,`stock`.`mfd`,`stock`.`exd` FROM `stock` INNER JOIN `grn_item` ON `grn_item`.`stock_id`=`stock`.`id` INNER JOIN `product` ON `stock`.`product_id` = `product`.`id` INNER JOIN `brand` ON `product`.`brand_id` = `brand`.`id` INNER JOIN `category` ON `product`.`category_id` = `category`.`id` " + wherequery + " ORDER BY " + sortquery + "");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("stock.id"));
                v.add(rs.getString("product.id"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("stock.quantity"));
                v.add(rs.getString("grn_item.buying_price"));
                v.add(rs.getString("stock.selling_price"));
                if (rs.getString("stock.mfd") == null) {
                    v.add("Disable");
                    v.add("Disable");
                } else {
                    v.add(rs.getString("stock.mfd"));
                    v.add(rs.getString("stock.exd"));
                }
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

