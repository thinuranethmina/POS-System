    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            SimpleDateFormat sdf3 = new SimpleDateFormat("yyyy-MM-dd  hh:mm:ss aa");

            InputStream pathStream = Invoice.class.getResourceAsStream("/reports/stock.jasper");

            if (text2.equals("")) {
                text2 = "All Products.";
            }

            HashMap parameters = new HashMap();
            parameters.put("Parameter1", sdf3.format(new Date()));
            parameters.put("Parameter2", text2);

            DefaultTableModel dtm2 = (DefaultTableModel) jTable1.getModel();
            Vector v2 = new Vector();

            for (int i = 0; i < dtm2.getRowCount(); i++) {
                StockReport sr = new StockReport(jTable1.getValueAt(i, 0).toString(), jTable1.getValueAt(i, 2).toString(), jTable1.getValueAt(i, 3).toString(), jTable1.getValueAt(i, 4).toString(), jTable1.getValueAt(i, 5).toString(), "Rs." + jTable1.getValueAt(i, 6).toString(), "Rs." + jTable1.getValueAt(i, 7).toString(), jTable1.getValueAt(i, 8).toString(), jTable1.getValueAt(i, 9).toString());
                v2.add(sr);
            }
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(v2);

            JasperPrint jp = JasperFillManager.fillReport(pathStream, parameters, dataSource);

            JasperPrintManager.printReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed


