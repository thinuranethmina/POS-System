    public static Thread setTime(javax.swing.JLabel timeLabel, SimpleDateFormat sdf) {
        Runnable rp = new Runnable() {
            @Override
            public void run() {

                while (true) {
                    Date d = new Date();
                    String date = sdf.format(d);
                    timeLabel.setText(date);
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
            }

        };
        Thread t = new Thread(rp);
        t.start();
        return t;

    }

