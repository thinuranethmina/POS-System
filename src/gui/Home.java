    public void chekAttendnce() {
        try {
            ResultSet rs1 = MySQL.search("SELECT * FROM `attendence` WHERE `user_id` = " + SignIn.userId + " ORDER BY `working_date` DESC ");
            if (rs1.next()) {
                if (sdf2.format(sdf2.parse(rs1.getString("working_date"))).equals(sdf2.format(logedTime))) {

                    if (rs1.getString("departure_time") != null) {
                        MarkAttendance.attendace_status = 0;
                    } else {
                        MarkAttendance.attendace_status = 1;
                    }

                } else {
                    MarkAttendance.attendace_status = 0;
                }

            } else {
                MarkAttendance.attendace_status = 0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

